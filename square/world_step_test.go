func worldMapToString(worldMap [][]rune) string {
	var buffer bytes.Buffer

	for h:=0; h<len(worldMap); h++ {
		for w:=0; w<len(worldMap[h]); w++ {

			buffer.WriteRune(worldMap[h][w])
		}
		buffer.WriteRune('\n')
	}

	return buffer.String()
}

